package com.braintribe.model.generic.path;

import java.util.Map;

public class ModelPathMapEntry<K, V> implements Map.Entry<K, V> {
	K key;
	V value;

	public ModelPathMapEntry(K key, V value) {
		this.key = key;
		this.value = value;
	}


	@Override
	public K getKey() {
		return key;
	}

	@Override
	public V getValue() {
		return value;
	}

	@Override
	public V setValue(V value) {
		this.value = value;
		return value;
	}

}
